    public Vendas viewBean(){
    Vendas vendas = new Vendas();
    int idV = Integer.valueOf(jTxtNVenda.getText());
    vendas.setIdVendas(idV);
    SimpleDateFormat formato = new  SimpleDateFormat("dd/MM/yyyy");
    try{
    vendas.setData(formato.parse(jFmtData.getText()));
    } catch(ParseException ex){
    Logger.getLogger(JDlgVendas.class.getName()).log(Level.SEVERE, null, ex);
        System.out.println("Erro MR Etica "+ ex.getMessage());
    }
     Cliente clientes1 = (Cliente) jCboClientes.getSelectedItem();
    vendas.setFkCliente(clientes1.getIdCliente());
    
    Vendedor vendedor1 = (Vendedor) jCboVendedor.getSelectedItem();
    vendas.setFkVendedor(vendedor1.getIdVendedor());
    vendas.setTotal(Double.parseDouble(jTxtTotal.getText()));   
        return vendas;
    };
    
        public void beanView(Vendas vendas){
        jTxtNVenda.setText(String.valueOf(vendas.getIdVendas()));
       
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtData.setText(formato.format(vendas.getData()));
        
        int idCliente = vendas.getFkCliente();
        Clientes_DAO clientes_DAO = new Clientes_DAO();
        jCboClientes.setSelectedItem((Cliente) clientes_DAO.list(idCliente));
       
        int idVendedor = vendas.getFkVendedor();
        Vendedor_DAO vendedor_DAO = new Vendedor_DAO();
        jCboVendedor.setSelectedItem((Vendedor)vendedor_DAO.list(idVendedor));
        
        jTxtTotal.setText(String.valueOf(vendas.getTotal()));
        
    }

